                    float lottaryPayOutReport = Float.parseFloat(lottaryPayOut.getText());
                    float lottaryPayOutBO = Float.parseFloat(boLottaryPO.getText());
                    float SOLottaryPayOut = lottaryPayOutReport - lottaryPayOutBO;

