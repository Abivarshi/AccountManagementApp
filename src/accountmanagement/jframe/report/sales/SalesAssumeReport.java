    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        Date currentDate = new Date();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        if (jDateChooserFrom.getDate() == null) {
            jDateChooserFrom.setDate(currentDate);
        }
        if (jDateChooserTo.getDate() == null) {
            jDateChooserTo.setDate(currentDate);
        }
        String fromDate = sdf.format(jDateChooserFrom.getDate());
        String toDate = sdf.format(jDateChooserTo.getDate());

        if (fromDate.compareTo(toDate) < 0 || fromDate.compareTo(toDate) == 0) {
            List<Map<String, String>> data = new ArrayList();
            List<Object[]> dataVal = new ArrayList();

            try {
                String description = "Date: " + fromDate + " - " + toDate;
                HashMap<String, Float> assumePercentage = db.getAdminProfit(shopName);
                
                String Purchase = "Assume Purchase ("+Float.toString(100 * assumePercentage.get("PurchasePercentage"))+"%)";
                String Expenditure = "Assume Expenditure ("+Float.toString(100 * assumePercentage.get("GrossProfitPercentage"))+"%)";
                String NetProfit = "Assume NetProfit ("+Float.toString(100 * assumePercentage.get("NetProfitPercentage"))+"%)";

                List<String> colNameList = new ArrayList();
                colNameList.add("AssumePurchase");
                colNameList.add("AssumeExpenditure");
                colNameList.add("AssumeNetProfit");
                colNameList.add("SalesSubTotal");

                ResultSet res1 = db.getNColValueTabTable(shopName, "Till", colNameList, fromDate, toDate);
                ResultSet res2 = db.getOneColValueTabTable(shopName, "Sales", "CustomerCount", fromDate, toDate);
                while (res1.next()) {
                    Float AssumePurchase = res1.getFloat("AssumePurchase");
                    Float AssumeExpenditure = res1.getFloat("AssumeExpenditure");
                    Float AssumeNetProfit = res1.getFloat("AssumeNetProfit");
                    Float SalesSubTotal = res1.getFloat("SalesSubTotal");
                    String date = res1.getString("Date");

                    dataVal.add(new Object[]{date, SalesSubTotal, 0, AssumePurchase, AssumeExpenditure, AssumeNetProfit});

                }
                while (res2.next()) {
                    String CustomerCount = res2.getString("CustomerCount");
                    String date = res2.getString("Date");
                    boolean valueAdded = false;
                    for (Object[] val : dataVal) {
                        if (val[0].toString().equals(date)) {
                            val[2] = Float.parseFloat(CustomerCount);
                            valueAdded = true;
                            break;
                        }
                    }
                    if (!valueAdded) {
                        dataVal.add(new Object[]{date, 0, CustomerCount, 0, 0, 0});
                    }
                }

                for (Object[] val : dataVal) {
                    Map<String, String> map = new HashMap();
                    map.put("Date", val[0].toString());
                    map.put("DaySales", val[1].toString());
                    map.put("Customer", val[2].toString());
                    map.put("Purchase", val[3].toString());
                    map.put("Expenditure", val[4].toString());
                    map.put("NetProfit", val[5].toString());
                    map.put("PurchaseCol", Purchase);
                    map.put("ExpenditureCol", Expenditure);
                    map.put("NetProfitCol", NetProfit);
                    map.put("description", description);
                    data.add(map);
                }
                if (!data.isEmpty()) {
                    JRDataSource dataSource = new JRBeanCollectionDataSource(data);
                    InputStream sourceName = getClass().getResourceAsStream("/accountmanagement/jframe/report/sales/SalesAssumeReport.jrxml");

                    JasperReport jasperReport = JasperCompileManager.compileReport(sourceName);
                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, null, dataSource);

                    JRViewer viewer = new JRViewer(jasperPrint);
                    jPanel1.add(viewer);
                    CardLayout layout = (CardLayout) jPanel1.getLayout();
                    layout.next(jPanel1);
                } else {
                    warningLabel1.setText("No record available within date " + fromDate + " - " + toDate);
                }
            } catch (Exception ex) {
                Logger.getLogger(SalesAssumeReport.class.getName()).log(Level.SEVERE, null, ex);
                warningLabel1.setText("Error while Generating report for date " + fromDate + " - " + toDate);
            }

        } else {
            warningLabel1.setText("From Date should be earlier date");
        }
    }//GEN-LAST:event_searchButtonActionPerformed


